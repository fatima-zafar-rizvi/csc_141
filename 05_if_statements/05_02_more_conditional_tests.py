
# Test using the lower() method:
car = 'BMW'
print("\nI think my dad's favorite car is bmw.")
print(car.lower() == 'bmw')

print("\nI think my dad's favorite car is bmw.")
print(car == 'bmw')

# Test using equality:
magic_number = 27
print("\nThe magic number is 27.")
print(magic_number == 27)

print("\nThe magic number is 7.")
print(magic_number == 7)

# Test using inequality:
lucky_number = 7
print("\nThe lucky number is not 5.")
print(lucky_number != 5)

print("\nThe lucky number is 5.")
print(lucky_number == 5)
